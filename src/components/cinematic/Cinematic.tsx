        <line x1={x} y1={590} x2={x} y2={596} stroke={FOREST} strokeWidth="0.5" opacity="0.35" />
        {x % 200 === 0 && (
          <>
            <text x={x} y={18} fontSize="7" fill={FOREST} opacity="0.5" fontFamily="ui-monospace, monospace" textAnchor="middle">
              {x}
            </text>
            <text x={x} y={584} fontSize="7" fill={FOREST} opacity="0.5" fontFamily="ui-monospace, monospace" textAnchor="middle">
              {x}
            </text>
          </>
        )}
