interface StreamProps {
  id: string;
  d: string;
  label: string;
  sublabel: string;
  visible: boolean;
  delay?: number;
  labelX?: number;
  labelY?: number;
  color?: "forest" | "sage";
}

function Stream({ id, d, label, sublabel, visible, delay = 0.2, labelX, labelY, color = "forest" }: StreamProps) {
  // Auto-derive label position from path midpoint if not specified
  const stroke = color === "sage" ? SAGE : FOREST;
  const marker = color === "sage" ? "url(#arrow-sage)" : "url(#arrow)";

  return (
    <motion.g
      initial={{ opacity: 0 }}
      animate={{ opacity: visible ? 1 : 0 }}
      transition={{ duration: 0.3 }}
    >
      <motion.path
        d={d}
        stroke={stroke}
        strokeWidth="1.5"
        fill="none"
        markerEnd={marker}
        initial={{ pathLength: 0 }}
        animate={{ pathLength: visible ? 1 : 0 }}
        transition={{ duration: 0.9, delay: visible ? delay : 0, ease: [0.16, 1, 0.3, 1] }}
      />
      {/* Stream tag - diamond */}
      <StreamTag
        id={id}
        path={d}
        delay={delay + 0.6}
        visible={visible}
        color={color}
        x={labelX}
        y={labelY}
      />
      <StreamLabel
        path={d}
        label={label}
        sublabel={sublabel}
        delay={delay + 0.7}
        visible={visible}
        x={labelX}
        y={labelY}
      />
    </motion.g>
  );
}

function StreamTag({
  id,
  path,
  delay,
  visible,
  color,
  x,
  y,
}: {
  id: string;
  path: string;
  delay: number;
  visible: boolean;
  color: "forest" | "sage";
  x?: number;
  y?: number;
}) {
  const stroke = color === "sage" ? SAGE : FOREST;
  // Estimate diamond position from start of path
  const m = path.match(/M\s*([\d.]+)\s+([\d.]+)/);
  const tx = x ?? (m ? parseFloat(m[1]) + 22 : 0);
  const ty = y ?? (m ? parseFloat(m[2]) - 12 : 0);
  return (
    <motion.g
      initial={{ opacity: 0 }}
      animate={{ opacity: visible ? 1 : 0 }}
      transition={{ duration: 0.4, delay: visible ? delay : 0 }}
    >
      <polygon
        points={`${tx},${ty - 8} ${tx + 16},${ty} ${tx},${ty + 8} ${tx - 16},${ty}`}
        fill={PAPER}
        stroke={stroke}
        strokeWidth="1"
      />
      <text x={tx} y={ty + 3} textAnchor="middle" fontSize="8" fontFamily="ui-monospace, monospace" fill={stroke}>
        {id}
      </text>
    </motion.g>
  );
}

function StreamLabel({
  path,
  label,
  sublabel,
  delay,
  visible,
  x,
  y,
}: {
  path: string;
  label: string;
  sublabel: string;
  delay: number;
  visible: boolean;
  x?: number;
  y?: number;
}) {
  // Estimate label position near path end
  const matches = Array.from(path.matchAll(/([\d.]+)\s+([\d.]+)/g)).map((m) => [
    parseFloat(m[1]),
    parseFloat(m[2]),
  ]);
  const start = matches[0] ?? [0, 0];
  const end = matches[matches.length - 1] ?? [0, 0];
  const mx = x ?? (start[0] + end[0]) / 2;
  const my = y ?? Math.min(start[1], end[1]) - 8;

  return (
    <motion.g
      initial={{ opacity: 0 }}
      animate={{ opacity: visible ? 1 : 0 }}
      transition={{ duration: 0.4, delay: visible ? delay : 0 }}
    >
      <text x={mx} y={my} textAnchor="middle" fontSize="10" fontFamily="ui-monospace, monospace" letterSpacing="1.4" fill={FOREST}>
        {label.toUpperCase()}
      </text>
      {sublabel && (
        <text x={mx} y={my + 11} textAnchor="middle" fontSize="8" fontFamily="ui-monospace, monospace" letterSpacing="1.2" fill={FOREST} opacity="0.65">
          {sublabel}
        </text>
      )}
    </motion.g>
  );
}

