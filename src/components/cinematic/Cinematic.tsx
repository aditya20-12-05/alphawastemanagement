const FOREST = "#0E2F23";
const SAGE = "#4A7C4E";
const LINE = "#D5CFBC";
const PAPER = "#F5F1E8";

export default function Cinematic() {
  const ref = useRef<HTMLElement>(null);
  const [activePhase, setActivePhase] = useState(0);
  const { scrollYProgress } = useScroll({
    target: ref,
    offset: ["start start", "end end"],
  });

  useMotionValueEvent(scrollYProgress, "change", (v) => {
    const idx = Math.min(PHASES.length - 1, Math.max(0, Math.floor(v * PHASES.length)));
    if (idx !== activePhase) setActivePhase(idx);
  });

  return (
    <section ref={ref} className="relative" style={{ height: "500vh" }}>
      <div className="sticky top-0 h-screen overflow-hidden bg-paper">
        <BlueprintGrid />
        <div className="absolute inset-0 grain opacity-30 pointer-events-none" />
        <CornerMarks />
        <TitleBlock activePhase={activePhase} />
        <RevisionStamp activePhase={activePhase} />

        <div className="absolute inset-0 grid place-items-center pt-12 pb-44 px-4">
          <PFD activePhase={activePhase} />
        </div>

        <ProgressBar activePhase={activePhase} />
        <Captions activePhase={activePhase} />
      </div>
    </section>
  );
}

